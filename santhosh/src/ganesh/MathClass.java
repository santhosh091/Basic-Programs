package ganesh;

public class MathClass {

	public static void main(String[] args) {
		
		System.out.println(Math.pow(2,8));
		System.out.println(Math.abs(-120.50));
		System.out.println(Math.sqrt(9));
		System.out.println(Math.cos(90));
		System.out.println(Math.random());
		System.out.println(Math.sin(180));
		System.out.println(Math.min(7,2));
		System.out.println(Math.min(9.2,9.1));
		System.out.println(Math.max(2,7));
		System.out.println(Math.max(2.7,7.2));
		System.out.println(Math.tan(90));
		System.out.println(Math.random()*10);
		System.out.println(Math.random()*100);
		System.out.println(Math.random()*1000);
		System.out.println(Math.random()*10000);
		System.out.println(Math.random()*100000);
		System.out.println(Math.random()*1000000);
		System.out.println((long)(Math.random()*1000000));

	}

}
